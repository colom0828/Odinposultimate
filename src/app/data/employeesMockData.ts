// ========================================
// HELPER
// ========================================
export function getEmployeesByBusinessType(businessType: BusinessType): Employee[] {
  switch (businessType) {
    case BusinessType.RESTAURANT:
    case BusinessType.BAR:
    case BusinessType.CAFE:
    case BusinessType.FAST_FOOD:
      return restaurantEmployees;
    
    case BusinessType.SPA:
      return spaEmployees;
    
    case BusinessType.HARDWARE:
    case BusinessType.RETAIL:
    case BusinessType.WHOLESALE:
      return hardwareEmployees;
    
    case BusinessType.TECH_SERVICE:
      return techServiceEmployees;
    
    default:
      return restaurantEmployees;
  }
}